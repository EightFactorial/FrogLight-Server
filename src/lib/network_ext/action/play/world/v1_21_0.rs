impl PlayChunkTrait for V1_21_0 {
    fn send_chunks(_chunks: &[(ChunkPosition, &Chunk)], task: &PlayTask<Self>) {
        // for (position, _chunk) in chunks {}

        for pos in ChunkPosition::splat(0).in_radius::<8>() {
            bevy::log::info!(target: "NEXT", "Sending chunk {:?}", pos);

            let mut data = Vec::new();
            FLAT_CHUNK.write_to(&mut data).unwrap();

            task.send(ChunkDataPacket {
                position: pos.into(),
                chunk_data: ChunkDataBuffer {
                    heightmaps: FLAT_HEIGHTMAP.clone(),
                    data,
                    entities: Vec::new(),
                },
                light_data: UnsizedBuffer::from_slice(LIGHT_DATA),
            });
        }
    }
}

